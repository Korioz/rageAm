#define DISTANCE(lhs, rhs) ((u64)(rhs) - (u64)(lhs))

#define MAX(lhs, rhs) ((lhs) > (rhs) ? (lhs) : (rhs))
#define MIN(lhs, rhs) ((lhs) > (rhs) ? (rhs) : (lhs))
